using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MathOperationMethodsAssignment_223
{
    class MathOperations
    {
        public static int DivideByThree(int usrInput)
        {
            return usrInput / 3;
        }
        public static int MultiplyByThree(int usrInput)
        {
            return usrInput * 3;
        }
        public static int AddThree(int usrInput)
        {
            return usrInput + 3;
        }
    }
}
